package sec02;

public class e1_연습 {

	public static void main(String[] args) {

		for (int i=0; i< 5; i++) {
			
			// 스페이스
			for(int j=0; j<i ; j++) {
				System.out.print("");
			}
			// 별
			for(int j=5; j>1 ; i-- )
			System.out.println("*");
		}
	System.out.println();
	}

}
