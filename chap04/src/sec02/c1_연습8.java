package sec02;

import java.util.Scanner;

public class c1_연습8 {

	public static void main(String[] args) {

		// 87에서 34까지 출력 (for문 사용)
		for (int i = 87; i >= 34; i--) {
			System.out.println(i);

			// 34부터 87까지 출력
			for (int a = 34; a <= 87; a++) {
				System.out.println(a);
			}
		}
	}
}
