package sec01;

public class Car {
	
	String company; // 제조 회사
	int cost; // 가격
	int maxSpeed; // 최고 속도
	String color; // 색상
	
	// 생성자
	public Car(String company, int cost, int maxSpeed, String color) {

		this.company = company;
		this.cost = cost;
		this.maxSpeed = maxSpeed;
		this.color = color;
	}
	
	
}
